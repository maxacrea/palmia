defined( 'ABSPATH' ) || exit;

global $product;

$attribute_keys  = array_keys( $attributes );
$variations_json = wp_json_encode( $available_variations );
$variations_attr = function_exists( 'wc_esc_json' ) ? wc_esc_json( $variations_json ) : _wp_specialchars( $variations_json, ENT_QUOTES, 'UTF-8', true );

do_action( 'woocommerce_before_add_to_cart_form' ); ?>
    <form x-data="{
        datas: <?php echo $variations_attr; // WPCS: XSS ok. ?>,
        handleClic($el) {
           	let variations = document.querySelectorAll('.cs-thumb');

		   	variations.forEach(
				function(currentValue, currentIndex, listObj) {
					let currentEl = $el.getAttribute('variation') !== currentValue.getAttribute('variation');
					if(currentValue.classList.contains('border-dark') && currentEl) currentValue.classList.remove('border-dark');
				}
			);

		   var inputVariation = document.querySelector('.variation_id');
		   inputVariation.setAttribute('value', parseInt($el.getAttribute('variation')));
           $el.classList.contains('border-dark') 
		   ? $el.classList.remove('border-dark')
		   : $el.classList.add('border-dark');
        }
    }"
        class="variations_form cart flex flex-col" action="<?php echo esc_url( apply_filters( 'woocommerce_add_to_cart_form_action', $product->get_permalink() ) ); ?>" method="post" enctype='multipart/form-data' data-product_id="<?php echo absint( $product->get_id() ); ?>" data-product_variations="<?php echo $variations_attr; // WPCS: XSS ok. ?>">

	<?php do_action( 'woocommerce_before_variations_form' ); ?>

	<?php if ( empty( $available_variations ) && false !== $available_variations ) : ?>
		<p class="stock out-of-stock"><?php echo esc_html( apply_filters( 'woocommerce_out_of_stock_message', __( 'This product is currently out of stock and unavailable.', 'woocommerce' ) ) ); ?></p>
	    <?php else : ?>
        <div class="flex gap-8 mb-10">
            <template x-for="attri, index in datas">
                <div class="cs-thumb h-32 w-32 border rounded" :class="index = 0 ? 'border-dark' : ''" :variation="attri.variation_id" @click="handleClic($el)">
                    <img :src="attri.image.thumb_src" />
                </div>
            </template>
        </div>
		<?php do_action( 'woocommerce_after_variations_table' ); ?>

		<div class="single_variation_wrap">
			<?php
				/**
				 * Hook: woocommerce_before_single_variation.
				 */
				do_action( 'woocommerce_before_single_variation' );

				/**
				 * Hook: woocommerce_single_variation. Used to output the cart button and placeholder for variation data.
				 *
				 * @since 2.4.0
				 * @hooked woocommerce_single_variation - 10 Empty div for variation data.
				 * @hooked woocommerce_single_variation_add_to_cart_button - 20 Qty and cart button.
				 */
				do_action( 'woocommerce_single_variation' );

				/**
				 * Hook: woocommerce_after_single_variation.
				 */
				do_action( 'woocommerce_after_single_variation' );
			?>
		</div>
	<?php endif; ?>

	<?php do_action( 'woocommerce_after_variations_form' ); ?>
</form>

<?php
do_action( 'woocommerce_after_add_to_cart_form' );
